import ast
import astor

from moske.ast.transformers import DeleteFunctionParts


def translate(
    python_file_path: str, delete_function_body: bool, delete_docstring: bool
) -> str:
    with open(python_file_path, "r") as python_file:
        file_ast = ast.parse(python_file.read())

    DeleteFunctionParts(delete_function_body, delete_docstring).visit(file_ast)

    return astor.to_source(file_ast)
